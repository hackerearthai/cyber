// ─── Risk band ───────────────────────────────────────────────────────────────

interface RiskBand {
  label: string;
  color: string;
  glow: string;
  bg: string;
  border: string;
}

function getRiskBand(score: number): RiskBand {
  if (score === 0)  return { label: "Safe",        color: "#10b981", glow: "shadow-emerald-200", bg: "bg-emerald-50",  border: "border-emerald-200" };
  if (score <= 25)  return { label: "Low Risk",    color: "#f59e0b", glow: "shadow-amber-200",   bg: "bg-amber-50",    border: "border-amber-200"   };
  if (score <= 55)  return { label: "Medium Risk", color: "#f97316", glow: "shadow-orange-200",  bg: "bg-orange-50",   border: "border-orange-200"  };
  if (score <= 80)  return { label: "High Risk",   color: "#ef4444", glow: "shadow-red-200",     bg: "bg-red-50",      border: "border-red-200"     };
  return                    { label: "Critical",   color: "#991b1b", glow: "shadow-red-300",     bg: "bg-red-100",     border: "border-red-300"     };
}

// ─── Riskometer ──────────────────────────────────────────────────────────────

function Riskometer({ score, band }: { score: number; band: RiskBand }) {
  const cx = 110, cy = 100, r = 80;
  const angle = 180 - (score / 100) * 180;
  const rad = (angle * Math.PI) / 180;
  const needleLength = 68;
  const nx = cx + needleLength * Math.cos(rad);
  const ny = cy - needleLength * Math.sin(rad);

  function arcPath(startPct: number, endPct: number, radius: number) {
    const s = (startPct / 100) * Math.PI;
    const e = (endPct / 100) * Math.PI;
    const x1 = cx + radius * Math.cos(Math.PI - s);
    const y1 = cy - radius * Math.sin(Math.PI - s);
    const x2 = cx + radius * Math.cos(Math.PI - e);
    const y2 = cy - radius * Math.sin(Math.PI - e);
    const large = endPct - startPct > 50 ? 1 : 0;
    return `M ${x1} ${y1} A ${radius} ${radius} 0 ${large} 1 ${x2} ${y2}`;
  }

  const segments = [
    { start: 0,  end: 25,  color: "#10b981" },
    { start: 25, end: 55,  color: "#f59e0b" },
    { start: 55, end: 80,  color: "#f97316" },
    { start: 80, end: 100, color: "#ef4444" },
  ];

  return (
    <svg viewBox="0 0 220 115" className="w-full max-w-[260px]" aria-label={`Threat score ${score}`}>
      <path d={arcPath(0, 100, r)} fill="none" stroke="#e2e8f0" strokeWidth="14" strokeLinecap="round" />
      {segments.map((seg) => (
        <path key={seg.color} d={arcPath(seg.start, seg.end, r)} fill="none" stroke={seg.color} strokeWidth="14" strokeLinecap="butt" opacity="0.35" />
      ))}
      {score > 0 && (
        <path d={arcPath(0, score, r)} fill="none" stroke={band.color} strokeWidth="14" strokeLinecap="round" />
      )}
      {[0, 25, 55, 80, 100].map((tick) => {
        const tr = (tick / 100) * Math.PI;
        const innerR = r - 10, outerR = r + 4;
        const tx1 = cx + innerR * Math.cos(Math.PI - tr);
        const ty1 = cy - innerR * Math.sin(Math.PI - tr);
        const tx2 = cx + outerR * Math.cos(Math.PI - tr);
        const ty2 = cy - outerR * Math.sin(Math.PI - tr);
        return <line key={tick} x1={tx1} y1={ty1} x2={tx2} y2={ty2} stroke="#cbd5e1" strokeWidth="1.5" />;
      })}
      <line x1={cx} y1={cy} x2={nx} y2={ny} stroke={band.color} strokeWidth="3" strokeLinecap="round" />
      <circle cx={cx} cy={cy} r="6" fill={band.color} />
      <circle cx={cx} cy={cy} r="3" fill="white" />
      <text x={cx} y={cy - 18} textAnchor="middle" fontSize="22" fontWeight="800" fill={band.color} fontFamily="system-ui, sans-serif">{score}</text>
      <text x={cx} y={cy - 5} textAnchor="middle" fontSize="7" fill="#94a3b8" fontFamily="system-ui, sans-serif" letterSpacing="1">THREAT SCORE</text>
      <text x="16" y="112" fontSize="7" fill="#94a3b8" fontFamily="system-ui, sans-serif">Safe</text>
      <text x="192" y="112" fontSize="7" fill="#94a3b8" textAnchor="end" fontFamily="system-ui, sans-serif">Critical</text>
    </svg>
  );
}

// ─── Result type ─────────────────────────────────────────────────────────────

interface Result {
  name: string;
  description: string;
  severity: Severity;
  weight: number;
  vulnerable: boolean;
}

function computeThreatScore(results: Result[]): number {
  const maxScore = TESTS.reduce((sum, t) => sum + t.weight, 0);
  const hitScore = results.filter((r) => r.vulnerable).reduce((sum, r) => sum + r.weight, 0);
  return Math.min(100, Math.round((hitScore / maxScore) * 100));
}

