// ─── Helpers ────────────────────────────────────────────────────────────────

function getDomain(url: string): string {
  try { return new URL(url).hostname; } catch { return url; }
}

function calcEntropy(str: string): number {
  const freq: Record<string, number> = {};
  for (const c of str) freq[c] = (freq[c] || 0) + 1;
  return -Object.values(freq).reduce((sum, f) => {
    const p = f / str.length;
    return sum + p * Math.log2(p);
  }, 0);
}

function countSubdomains(url: string): number {
  return getDomain(url).split(".").length - 2;
}

function countSpecialChars(url: string): number {
  return (url.match(/[@%#~]/g) || []).length;
}

function hasLookalikeTrick(url: string): boolean {
  const domain = getDomain(url).toLowerCase();
  const brands = ["paypal","amazon","google","facebook","microsoft","apple","netflix","instagram","twitter","linkedin","whatsapp","ebay","bank","chase","wellsfargo","citibank"];
  const normalized = domain.replace(/0/g,"o").replace(/1/g,"l").replace(/3/g,"e").replace(/4/g,"a").replace(/5/g,"s");
  return brands.some(b => normalized.includes(b) && !domain.includes(b));
}

function hasAtSymbolTrick(url: string): boolean {
  try { return new URL(url).username !== "" || url.includes("@"); }
  catch { return url.includes("@"); }
}

function hasExcessiveDots(url: string): boolean {
  return (getDomain(url).match(/\./g) || []).length >= 4;
}

function hasNumericSubdomain(url: string): boolean {
  return getDomain(url).split(".").some(p => /^[a-z]{0,2}\d{5,}$/.test(p));
}

function isEmailTrackingAbuse(url: string): boolean {
  return ["sendgrid.net","ct.sendgrid","mailchimp.com","list-manage.com","mandrillapp.com","emltrk.com","click.mlsend","trk.","r.mail.","email.mg."]
    .some(s => url.toLowerCase().includes(s));
}

function isAutoGeneratedDomain(url: string): boolean {
  const domain = getDomain(url).split(".")[0];
  return /\d{5,}/.test(domain) || (/[a-z]\d{3,}/.test(domain) && domain.length > 8);
}

function hasEncodedHiddenURL(url: string): boolean {
  return (url.match(/-2F|-2B|-3D|-2C|-3A/gi) || []).length >= 3;
}

function hasNestedURL(url: string): boolean {
  try {
    const params = new URL(url).search;
    return /upn=|url=|u=|link=/.test(params) && params.length > 50;
  } catch { return false; }
}

function isClickTrackingPath(url: string): boolean {
  const paths = ["/ls/click","/click","/track/click","/wf/click","/c/","/l/","/lt.php","/click.php","/go/","/redirect","/r/","/out/"];
  try {
    const path = new URL(url).pathname.toLowerCase();
    return paths.some(p => path.startsWith(p) || path === p);
  } catch { return false; }
}

function hasSuspiciousParams(url: string): boolean {
  try {
    const params = new URL(url).search.toLowerCase();
    const suspicious = ["upn=","u=","url=","link=","dest=","goto=","target=","redir=","ref="];
    return suspicious.filter(p => params.includes(p)).length >= 1 && params.length > 80;
  } catch { return false; }
}

function hasMultipleRedirects(url: string): boolean {
  const redirectParams = ["redirect=","url=","next=","dest=","goto=","link=","target="];
  return redirectParams.filter(p => url.toLowerCase().includes(p)).length >= 2;
}

function isSendGridRedirectAbuse(url: string): boolean {
  const isTracking = ["sendgrid.net","ct.sendgrid","mailchimp.com","list-manage.com"]
    .some(s => url.toLowerCase().includes(s));
  let isClick = false;
  try {
    const pn = new URL(url).pathname.toLowerCase();
    isClick = ["/ls/click","/click","/track/click"].some(p => pn.startsWith(p));
  } catch {}
  return isTracking && isClick;
}

